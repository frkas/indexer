package org.alfresco.consulting.indexer.webscripts;

public class BreadCrumb {

	private String url = "";
	private String label = "";
	
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
}
